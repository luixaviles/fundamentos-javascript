// VARIABLES

// Declarar variables
// let, const

// let <nombre> = <valor>
let movie = 'Spiderman';
console.log('movie', movie); // JS runtime
movie = 'Superman';
console.log('movie', movie); // JS runtime

// const <nombre> = <valor>
const course = 'JavaScript';
console.log('course', course);
// course = 'TypeScript'; // Error

// Cuando uso let o const?
let visitCounter = 10;
visitCounter = 20;
visitCounter = 30;
console.log('visitCounter', visitCounter);
